def diagonalDifference(arr):
    # Write your code here
    sums = 0
    sums2 = 0
    for i in range(0,len(arr)):
        sums += arr[i][i]
        sums2 += arr[i][len(arr)-1-i]
    

    final = abs(sums - sums2)
    return final

